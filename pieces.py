class Piece:
  scale = 30
  rotation = 0
  x = 4
  y = 1

  def __init__(self):
    self.type = choice(list(matrix.keys()))
    self.matrix = matrix[self.type][self.rotation]
    self.bound = bound[self.type][self.rotation]

  # Roate shape
  def rotate(self):
    self.rotation += 1
    while self.rotation >= len(matrix[self.type]):
      self.rotation -= len(matrix[self.type])
    self.matrix = matrix[self.type][self.rotation]
    self.bound = bound[self.type][self.rotation]